<h2><strong>BABY MASSAGE</strong></h2>
<br>
<br>
<h3><p>When massaging your baby, use a small amount of an appropriately formulated oil, like baby oil —enough for your hands to glide comfortably over your baby’s skin.<p>
<p>
Massaging your baby with oil reduces friction, helping make your strokes smoother and more rhythmic. In a study comparing 1-month-old infants massaged with oil versus infants who were not, infants who were massaged with oil showed less fidgeting, less grimacing and less stress behavior (mouthing and clenching fists).</p>
</p>
But be careful when choosing an oil to use on your baby’s skin. Natural oils that aren’t specifically formulated for your baby’s skin, like store-bought cooking-grade olive oil, may damage the skin barrier, potentially drying out your baby’s skin. Pure pharmaceutical grade mineral oil, like that used in all of  baby oils, supports the protective function of the skin barrier and helps prevent water loss, relieving symptoms of dry skin while not clogging pores.</p></h3>
